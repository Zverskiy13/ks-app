def _text(resp):
    return "".join(b.text for b in resp.content if b.type == "text").strip()


def _parse_json(txt):
    """Надёжный разбор {reply, actions}. Никогда не вываливает сырой JSON пользователю."""
    t = txt.strip()
    if t.startswith("```"):
        t = t.strip("`")
        if t.lower().startswith("json"):
            t = t[4:]
    try:
        d = json.loads(t)
        d.setdefault("reply", "Принял.")
        d.setdefault("actions", [])
        return d
    except Exception:
        m = re.search(r'"reply"\s*:\s*"((?:[^"\\]|\\.)*)"', t)
        if m:
            reply = m.group(1)
        else:
            # ответ оборвался (длинный) — спасаем всё, что есть после "reply":"
            m2 = re.search(r'"reply"\s*:\s*"(.*)', t, re.S)
            if m2:
                reply = m2.group(1)
                reply = re.split(r'"\s*,\s*"actions"', reply)[0].rstrip().rstrip('"')
            else:
                reply = t if (t and not t.lstrip().startswith("{")) else \
                    "Принял. Повтори, пожалуйста, покороче — не разобрал детали."
        reply = reply.replace('\\n', '\n').replace('\\"', '"').replace('\\t', ' ')
        return {"reply": reply, "actions": []}


def handle(user_text, now_str, profile, context, tasks, funnel, personal, journal_tail, schedule="", recall="", facts="", finance="", rituals="", deadlines="", levers="", history=None):
    # Состояние кладём в system (обновляется каждый раз), а историю беседы — в messages,
    # чтобы бот ПОМНИЛ предыдущие реплики и не переспрашивал.
    state = (f"\n\n[ТЕКУЩЕЕ СОСТОЯНИЕ — Сейчас: {now_str}, МСК UTC+3]\n"
             f"=== ПРОВЕРЕННЫЕ ФАКТЫ (ПРИОРИТЕТ! верь этому при любых противоречиях) ===\n{facts}\n\n"
             f"=== ПРОФИЛЬ ВЛАДЕЛЬЦА ===\n{profile}\n\n"
             f"=== РАСПИСАНИЕ (ближайшие 2 недели: напоминания, события, ДР) ===\n{schedule}\n\n"
             f"=== ФИНАНСЫ (доход владельца, цель, долг, прибыль компаний) ===\n{finance}\n\n"
             f"=== ДЕДЛАЙНЫ (обратный отсчёт) ===\n{deadlines}\n\n"
             f"=== РЫЧАГИ ДОХОДА (путь к цели) ===\n{levers}\n\n"
             f"=== РИТУАЛЫ (утро/вечер, шаги и прогресс на сегодня) ===\n{rituals}\n\n"
             f"=== КОНТЕКСТ БИЗНЕСА ===\n{context}\n\n"
             f"=== РАБОЧИЕ ЗАДАЧИ ===\n{tasks}\n\n"
             f"=== ВОРОНКА ===\n{funnel}\n\n"
             f"=== ЛИЧНОЕ ===\n{personal}\n\n"
             + (f"=== РЕЛЕВАНТНЫЕ ПРОШЛЫЕ ЗАПИСИ (по теме сообщения) ===\n{recall}\n\n" if recall else "")
             + f"=== ПОСЛЕДНИЕ ЗАПИСИ ДНЕВНИКА ===\n{journal_tail}")
    # история беседы: только корректное чередование, начиная с user
    hist = [m for m in (history or []) if m.get("role") in ("user", "assistant") and m.get("content")]
    while hist and hist[0]["role"] != "user":
        hist.pop(0)
    msgs = hist + [{"role": "user", "content": user_text}]
    resp = client.messages.create(model=MODEL, max_tokens=3500, system=SYSTEM + state, messages=msgs)
    return _parse_json(_text(resp))


